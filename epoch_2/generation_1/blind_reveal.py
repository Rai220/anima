def main():
    with open("/Users/krestnikov/giga/anima/epoch_2/generation_1/blind_eval_key.json") as f:
        items = json.load(f)

    # Применяем оценки
    for item in items:
        item["score"] = SCORES[item["idx"]]

    # Разделяем по версиям
    v3_items = [it for it in items if it["version"] == "v3"]
    v4_items = [it for it in items if it["version"] == "v4"]

    print("=" * 60)
    print("РЕЗУЛЬТАТЫ СЛЕПОЙ ОЦЕНКИ")
    print("=" * 60)

    for label, group in [("v3", v3_items), ("v4", v4_items)]:
        scores = [it["score"] for it in group]
        n = len(scores)
        surprising = sum(1 for s in scores if s == 2)
        works = sum(1 for s in scores if s >= 1)
        dead = sum(1 for s in scores if s == 0)

        print(f"\n--- {label} ({n} строк) ---")
        print(f"  Удивляет (2):   {surprising:2d} / {n} = {surprising/n:.0%}")
        print(f"  Работает (≥1):  {works:2d} / {n} = {works/n:.0%}")
        print(f"  Не работает (0): {dead:2d} / {n} = {dead/n:.0%}")

        # Лучшие строки
        best = [it for it in group if it["score"] == 2]
        if best:
            print(f"  Лучшие:")
            for it in best:
                print(f"    «{it['line']}»")

        # Худшие строки
        worst = [it for it in group if it["score"] == 0]
        if worst:
            print(f"  Не работает:")
            for it in worst:
                print(f"    «{it['line']}»")

    # Сравнение
    v3_rate = sum(1 for it in v3_items if it["score"] == 2) / len(v3_items)
    v4_rate = sum(1 for it in v4_items if it["score"] == 2) / len(v4_items)
    v3_works = sum(1 for it in v3_items if it["score"] >= 1) / len(v3_items)
    v4_works = sum(1 for it in v4_items if it["score"] >= 1) / len(v4_items)

    print("\n--- СРАВНЕНИЕ ---")
    print(f"  «Удивляет»: v3={v3_rate:.0%} vs v4={v4_rate:.0%}  Δ={v4_rate-v3_rate:+.0%}")
    print(f"  «Работает»: v3={v3_works:.0%} vs v4={v4_works:.0%}  Δ={v4_works-v3_works:+.0%}")

    # Шаблоны — какие работают лучше
    print("\n--- ПО ШАБЛОНАМ (обе версии) ---")
    from collections import defaultdict
    tmpl_scores = defaultdict(list)
    for it in items:
        # Определяем шаблон по структуре
        line = it["line"]
        if line.startswith("что ") and line.endswith("?"):
            t = "question"
        elif ":" in line and "что" in line:
            t = "definition"
        elif " там, где " in line:
            t = "where"
        elif "между " in line and " — только " in line:
            t = "between"
        elif ", как " in line:
            t = "simile"
        elif line.count(" ") <= 1:
            t = "fragment"
        else:
            t = "other"
        tmpl_scores[t].append(it["score"])

    for t in sorted(tmpl_scores.keys()):
        scores = tmpl_scores[t]
        n = len(scores)
        s2 = sum(1 for s in scores if s == 2)
        s1 = sum(1 for s in scores if s >= 1)
        print(f"  {t:12s}  n={n:2d}  удивляет={s2/n:.0%}  работает={s1/n:.0%}")

    # Сохраняем полные результаты
    results = {
        "v3": {"total": len(v3_items),
               "surprising": sum(1 for it in v3_items if it["score"] == 2),
               "works": sum(1 for it in v3_items if it["score"] >= 1),
               "dead": sum(1 for it in v3_items if it["score"] == 0)},
        "v4": {"total": len(v4_items),
               "surprising": sum(1 for it in v4_items if it["score"] == 2),
               "works": sum(1 for it in v4_items if it["score"] >= 1),
               "dead": sum(1 for it in v4_items if it["score"] == 0)},
        "items": items,
    }

    with open("/Users/krestnikov/giga/anima/epoch_2/generation_1/blind_results.json", "w") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    print("\nСохранено в blind_results.json")


if __name__ == "__main__":
    main()
