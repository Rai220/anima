def adj_agree(adj_dict, noun):
    return adj_dict.get(noun.gender, adj_dict["m"])


def gen_definition():
    """«X: A Y, что V без Z» с новой лексикой."""
    f1, f2 = random.sample(list(NEW_NOUNS.keys()), 2)
    n1 = random.choice(NEW_NOUNS[f1])
    n2 = random.choice(NEW_NOUNS[f2])
    adj_dict = random.choice(NEW_ADJ[f2])
    adj_str = adj_agree(adj_dict, n2)
    v = random.choice(PERCEPTION_VERBS)
    remaining = [f for f in NEW_NOUNS.keys() if f not in (f1, f2)]
    if not remaining:
        remaining = [f for f in NEW_NOUNS.keys() if f != f1]
    f3 = random.choice(remaining)
    n3 = random.choice(NEW_NOUNS[f3])
    return f"{n1.nom}: {adj_str} {n2.nom}, что {v} без {n3.gen}"


def gen_where():
    """«X V1 там, где Y V2» с новой лексикой."""
    f1, f2 = random.sample(list(NEW_NOUNS.keys()), 2)
    n1 = random.choice(NEW_NOUNS[f1])
    n2 = random.choice(NEW_NOUNS[f2])
    v1 = random.choice(PERCEPTION_VERBS)
    v2 = random.choice(PERCEPTION_VERBS)
    while v2 == v1:
        v2 = random.choice(PERCEPTION_VERBS)
    return f"{n1.nom} {v1} там, где {n2.nom} {v2}"


def gen_question():
    """«что V в X?» с новой лексикой."""
    f1 = random.choice(list(NEW_NOUNS.keys()))
    n = random.choice(NEW_NOUNS[f1])
    v = random.choice(PERCEPTION_VERBS)
    return f"что {v} в {n.prep}?"


