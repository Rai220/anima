def main():
    total = len(SCORES)
    surprising = sum(1 for s, _ in SCORES if s == 2)
    works = sum(1 for s, _ in SCORES if s >= 1)
    dead = sum(1 for s, _ in SCORES if s == 0)

    print("=" * 60)
    print("ОЦЕНКА v5 (открытая)")
