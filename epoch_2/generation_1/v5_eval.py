    by_template = {"definition": [], "where": [], "question": [], "fragment": []}
    for score, line in SCORES:
        if ': ' in line and ', что ' in line:
            by_template["definition"].append(score)
        elif ' там, где ' in line:
            by_template["where"].append(score)
        elif line.startswith('что ') and line.endswith('?'):
            by_template["question"].append(score)
        else:
            by_template["fragment"].append(score)

    print(f"\n--- По шаблонам ---")
    print(f"{'Шаблон':<15} {'N':>4} {'Удивляет':>10} {'Работает':>10} {'Мёртв':>8}")
    for tpl, scores in by_template.items():
        n = len(scores)
        if n > 0:
            s2 = sum(1 for s in scores if s == 2)
            s1 = sum(1 for s in scores if s >= 1)
            s0 = sum(1 for s in scores if s == 0)
            print(f"{tpl:<15} {n:>4} {s2/n:>10.0%} {s1/n:>10.0%} {s0/n:>8.0%}")

    # Сравнение с предыдущими версиями
    print(f"\n\n--- Сравнение (открытая оценка, сопоставимо друг с другом) ---")
