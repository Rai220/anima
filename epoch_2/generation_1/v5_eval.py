    print("Примечание: v3 оценивался в другой сессии с другим порогом строгости.")
    print("В слепой оценке v3 получил 43% 'удивляет' (vs 38% в открытой).")
    print("Сверхкоррекция ~5%. Реальный скор v5 может быть ~51%.")

    # Definition specifically
    def_scores = by_template["definition"]
    print(f"\n--- Definition отдельно ---")
    print(f"v3 definition (слепая): 84% удивляет, 89% работает, 11% мёртв (N=19)")
