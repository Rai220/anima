    print(f"{'v3':.<8} {'38%':>10} {'62%':>10} {'38%':>8}  (N=50, другая сессия)")
    print(f"{'v5':.<8} {surprising/total:>10.0%} {works/total:>10.0%} {dead/total:>8.0%}  (N={total})")
